# put your python code here
a, b = int(input()), int(input())
n = 0
endS = 0
for i in range(b, a-1, -1) :
    sm = 0
    for j in range(1, i+1) :
        if i%j == 0 :
            sm += j
    if endS < sm :
        endS = sm
        n = i
print(n, endS)