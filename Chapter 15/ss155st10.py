for i in range(len(st)) :
    if st[i].isalpha() :
        count = len(st[i])
        for c in st[i] :
            n = ord(c.lower()) + count
            if n > 122 : n = n - 26
            if c.isupper() : tmp = tmp + chr(n).upper()
            else : tmp = tmp + chr(n)
        st[i] = tmp
        tmp = ""
